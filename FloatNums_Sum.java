public class FloatNums_Sum {
    public static void main(String[] args) {
        float sum =0f;
        float []nums = {20.3f,20.5f,32.3f,40.4f,50.5f};
        for(int i=0;i<nums.length;i++){

             sum = sum + nums[i];

        }
        System.out.println(sum);

    }



}
